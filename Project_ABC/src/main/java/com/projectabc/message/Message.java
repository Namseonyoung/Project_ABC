package com.projectabc.message;

public class Message {
	String mesgno;
	String sendid;
	String recvid;
	String mesgtitle;
	String mesgcont;
	String senddate;
	String isread;
	
	public String getMesgno() {
		return mesgno;
	}
	public void setMesgno(String mesgno) {
		this.mesgno = mesgno;
	}
	public String getSendid() {
		return sendid;
	}
	public void setSendid(String sendid) {
		this.sendid = sendid;
	}
	public String getRecvid() {
		return recvid;
	}
	public void setRecvid(String recvid) {
		this.recvid = recvid;
	}
	public String getMesgtitle() {
		return mesgtitle;
	}
	public void setMesgtitle(String mesgtitle) {
		this.mesgtitle = mesgtitle;
	}
	public String getMesgcont() {
		return mesgcont;
	}
	public void setMesgcont(String mesgcont) {
		this.mesgcont = mesgcont;
	}
	public String getSenddate() {
		return senddate;
	}
	public void setSenddate(String senddate) {
		this.senddate = senddate;
	}
	public String getIsread() {
		return isread;
	}
	public void setIsread(String isread) {
		this.isread = isread;
	}
	
}
